export const findGameById = (id) => {
  return allGames.find(game => game.id === id);
};

export const searchGames = (query) => {
  return allGames.filter(game => 
    game.name.toLowerCase().includes(query.toLowerCase())
  );
};

// Tambahkan fungsi untuk mendapatkan trending games (misalnya berdasarkan rating tertinggi)
export const getTrendingGames = (limit = 16) => {
  return [...allGames]
    .sort((a, b) => b.rating - a.rating)
    .slice(0, limit);
};

// Tambahkan fungsi untuk mendapatkan recommended games (misalnya random games)
export const getRecommendedGames = (limit = 16) => {
  return [...allGames]
    .sort(() => Math.random() - 0.5)
    .slice(0, limit);
};